
public class NaturalNUMBERS {
	public static void main(String[] args) 
	{ 
		 System.out.println("program to find natural numbers between 5 to 25:");
		int n=25 , sum=0, i=25;
		while(i<=n)
		{
			sum+=i;
			i++;
		}
		System.out.println("the  natural numbers between 5 to 25:" +sum);
		}
}
