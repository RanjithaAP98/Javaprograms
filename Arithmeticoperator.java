class Arithmeticoperator{
	public static void main(String arg[]){

		System.out.println("The addition of three variables");
		int num1=10;
		int num2=20;
		int num3=30;
		int sum=num1+num2+num3;
		System.out.println("The sum of three numbers:" +sum);

		
		System.out.println("The subtraction of three variables");
		int a=2, b=3,c=5;
		int diff= a-b-c;
		System.out.println("The difference of 3 numbers:" +diff);


		System.out.println("The Multiplication  of three variables");
		int m1=2, m2=6, m3=7;
		int product=m1*m2*m3;
		System.out.println("The product of three numbers:" +product);


		System.out.println("The Division  of three variables");
		double dnum1=10.56, dnum2=5.00, dnum3=15.01;
		double doubledivquotient= dnum1/dnum2/dnum3;
		System.out.println("The quotient of three num numbers 10.56  5.00 and 15.01 :" +doubledivquotient);

		System.out.println("The Reminder  of three variables");
		int reminder=77%24%7;
		System.out.println("The reminder of three num numbers 77 24 and 7 is :" +reminder);

		
	}
}



		