package com.ranjitha.javaclasses;

public class Constructors {
			  int x;  
		  
		  public Constructors() { //Constructors
		    x = 5;  
		  }

		  
		  
		  
		  
		  public static void main(String[] args) {
			  Constructors myObj = new Constructors(); 
		    System.out.println(myObj.x);
		  }

}
