class MarksRangeForTheGrade{
      public static void main(String args[]){
              char grade='A';

		switch(grade) {
				case 'A' :
					System.out.println("your % is between 85 to 100");
						break;
				case 'B' :
					System.out.println("your % is between 65 to 84");
						break;

				case 'C' :
					System.out.println("your % is between 55 to 64");
						break;

				case 'D' :
					System.out.println("your % is below 35");
						break;
				}
						}
}